public class Item2 {
    private int pid;
    private int totalQuantityOfItem;

    public Item2() {
    }

    public Item2(int pid, int totalQuantityOfItem) {
        this.pid = pid;
        this.totalQuantityOfItem = totalQuantityOfItem;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getTotalQuantityOfItem() {
        return totalQuantityOfItem;
    }

    public void setTotalQuantityOfItem(int totalQuantityOfItem) {
        this.totalQuantityOfItem = totalQuantityOfItem;
    }
    
    
}
