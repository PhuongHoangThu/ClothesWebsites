package Model;

/**
 *
 * @author HP
 */
public class QuantityMonth {
    private int year;
    private int month;
    private Category category;
    private int quantity;

    public QuantityMonth() {
    }

    public QuantityMonth(int year, int month, Category category, int quantity) {
        this.year = year;
        this.month = month;
        this.category = category;
        this.quantity = quantity;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
   
}
