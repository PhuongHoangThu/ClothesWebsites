package Model;

/**
 *
 * @author HP
 */
public class ChartYear {
    private int year;
    private int quantity;

    public ChartYear() {
    }

    public ChartYear(int year, int quantity) {
        this.year = year;
        this.quantity = quantity;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
     
}
